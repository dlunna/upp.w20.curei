    <h1>Consulta de Medicina General y Especialidad en Geriatría, Consulta de Nutrición</h1>

    <p>El servicio de Nutrición ofertado en el C.U.Re.I se encuentra a cargo del L.N. Erick Alexis Sánchez Tapia, quien se encarga de evaluar la composición corporal de los pacientes mediante la toma de antropometría (peso, talla, circunferencia de cintura), con el objetivo de brindar una atención integral a los solicitantes, en un trabajo interdisciplinario con los servicios de medicina y rehabilitación también ofertados; una vez establecido el diagnóstico corporal, se realiza la toma del historial clínico detallado de cada paciente y con base en ello se plantea un programa de alimentación individualizado que habrá de llevarse a cabo por un lapso de 4 semanas, además de que, durante la consulta nutricional, se brinda información y orientación alimentaria así como promoción de la salud a cada paciente.</p>
    <p>Dentro de la consulta subsecuente se realiza de nuevo la valoración antropométrica para conocer los resultados de la intervención e integrar y proponer nuevas estrategias para el cumplimiento del objetivo nutricional de cada paciente.</p>

