      <h1>CLÍNICA UNIVERSITARIA DE REHABILITACIÓN INTEGRAL (C.U.Re.I)</h1>

      <h2>Misión</h2>
      <p>Somos una clínica de rehabilitación  que brinda atención y servicios de salud al público en general, enfocados a la prevención, tratamiento y rehabilitación de personas con limitaciones físicas, funcionales e intelectuales, para impulsar la recuperación, potenciación y mantenimiento de sus capacidades, e integrar a la persona de manera interpersonal, laboral y social mejorando su calidad de vida.</p>

      <h2>Visión</h2>
      <p>Convertirse en referente para las Universidades Politécnicas de nuestro país que oferten la Licenciatura en Terapia Física en la atención de pacientes y en la preparación del futuro de los egresados de nuestra Institución.</p>

      <h1>Acerca de</h1>

      <p>La Clínica Universitaria de Rehabilitación Integral se encuentra en la Universidad Politécnica de Pachuca y presta servicios de medicina enfocada al primer nivel de atención.</p>
      <p>Los servicios de esta Clínica están dirigidos a estudiantes, docentes, administrativos y al público en general, a quienes se ofrece atención de consulta general y de especialidad, consulta de nutrición y terapia física.</p>
      <p>C.U.Re.I. cuenta con espacio para consultorio, área de mecanoterapia, circuito de marcha al aire libre, área de agentes físicos, área de hidroterapia, sala de espera y recepción. Cuenta con equipo necesario para brindar dichos servicios.</p>
      <p>Todas las consultas son atendidas por profesionales titulados altamente preparados y capacitados para evaluar, prevenir y establecer tratamientos de precisión, global, especializado e individualizado, promoviendo la calidad de vida de cada uno de nuestros usuarios.</p>
      <p>Cada paciente da su consentimiento para que 2 o 3 estudiantes de la Licenciatura en Terapia Física puedan hacer sus observaciones y prácticas asignadas por docentes del programa educativo. Además de contarse con estudiantes de Estadía que realizan sus prácticas profesionales en la Clínica.</p>
      <p>Nuestro sistema consultorio-laboratorio permite que el usuario y la práctica estudiantil se puedan llevar a cabo de forma profesional, ya que los estudiantes están supervisados por los médicos y licenciados en turno.</p>

